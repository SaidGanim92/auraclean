function normalizeHe(s: string): string {
  return s
    .replace(/\u200f|\u200e/g, '')
    .replace(/[׳'’]/g, "'")
    .replace(/\s+/g, ' ')
    .trim();
}

/** האם נשארו אותיות עבריות שדורשות תרגום נוסף */
function stillHasHebrew(s: string): boolean {
  return /[\u0590-\u05FF]/.test(s);
}

/**
 * תרגום שם מוצר: מילון + מותגים.
 * אם נשארה עברית — קורא ל-fallback (למשל Google) רק על השאריות.
 */
export async function translateProductName(
  nameHe: string,
  fallback?: (text: string) => Promise<string>
): Promise<string> {
  let s = normalizeHe(nameHe || '');
  if (!s) return '';

  // 1) מונחים ומותגים נשמרים כמציינים זמניים לפני תרגום השאריות.
  // כך Google אינו מקבל משפט מעורב עברית/ערבית שעלול להפוך "גל כביסה"
  // ל"מوجة الغسيل" או לשבש תרגום שכבר עבר במילון.
  const saved: string[] = [];
  const preserve = (entries: [RegExp, string][]) => entries.forEach(([re, ar]) => {
    s = s.replace(re, () => {
      const i = saved.length;
      saved.push(ar);
      // רווחים סביב המציין מונעים חיבור של התרגום החופשי למונח השמור.
      return ` «P${i}» `;
    });
  });
  preserve(BRANDS);

  // 2) גם מונחי ניקיון נשמרים, ולא רק מותגים.
  preserve(TERMS);

  // 3) שאריות עברית → fallback או השארה
  if (stillHasHebrew(s) && fallback) {
    // מפרקים לטוקנים עבריים ומנסים לתרגם רק אותם
    const parts = s.split(/(«P\d+»)/);
    const out: string[] = [];
    for (const part of parts) {
      if (/^«P\d+»$/.test(part)) {
        out.push(part);
        continue;
      }
      if (!stillHasHebrew(part)) {
        out.push(part);
        continue;
      }
      try {
        const prefix = /^\s/.test(part) ? ' ' : '';
        const suffix = /\s$/.test(part) ? ' ' : '';
        const tr = (await fallback(part.trim())).trim();
        // אם ה-fallback החזיר עברית / ריק — משאירים את המקור העברי עדיף מכלום מצחיק
        out.push(prefix + (tr && !looksLikeHebrew(tr) ? tr : part.trim()) + suffix);
      } catch {
        out.push(part);
      }
    }
    s = out.join('');
  }

  // 4) שחזור מותגים
  s = s.replace(/«P(\d+)»/g, (_, n) => saved[Number(n)] || '');

  // ניקוי רווחים/מקפים כפולים
  s = s.replace(/\s{2,}/g, ' ').replace(/\s+([,.\-–])/g, '$1').trim();
  return s;
}

/** תרגום קטגוריה קצרה — אותו מילון בלי fallback כבד */
export async function translateCategoryName(
  catHe: string,
  fallback?: (text: string) => Promise<string>
): Promise<string> {
  return translateProductName(catHe, fallback);
}
